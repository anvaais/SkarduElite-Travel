const RTL_LANGS = ['ur', 'ar'];
const LANG_LABELS = {en:'EN',ur:'UR',ar:'AR',zh:'ZH',es:'ES',fr:'FR',de:'DE',ja:'JA',ko:'KO',tr:'TR',ru:'RU',it:'IT'};

function applyLanguage(lang) {
  const t = TRANSLATIONS[lang];
  if (!t) return;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key]) {
      if (t[key].includes('<')) { el.innerHTML = t[key]; } else { el.textContent = t[key]; }
    }
  });
  const dir = RTL_LANGS.includes(lang) ? 'rtl' : 'ltr';
  document.documentElement.setAttribute('dir', dir);
  document.documentElement.setAttribute('lang', lang);
  const langCurrent = document.getElementById('langCurrent');
  if (langCurrent) langCurrent.textContent = LANG_LABELS[lang] || lang.toUpperCase();
  document.querySelectorAll('.lang-option').forEach(opt => {
    opt.classList.toggle('active', opt.dataset.lang === lang);
  });
  localStorage.setItem('skardu_lang', lang);
}

function initLanguageSelector() {
  const selector = document.getElementById('langSelector');
  const btn = document.getElementById('langBtn');
  const dropdown = document.getElementById('langDropdown');
  if (!selector || !btn || !dropdown) return;

  btn.addEventListener('click', (e) => {
    e.stopPropagation();
    selector.classList.toggle('open');
    btn.setAttribute('aria-expanded', selector.classList.contains('open'));
  });

  document.querySelectorAll('.lang-option').forEach(opt => {
    opt.addEventListener('click', (e) => {
      e.stopPropagation();
      const lang = opt.dataset.lang;
      applyLanguage(lang);
      selector.classList.remove('open');
      btn.setAttribute('aria-expanded', 'false');
    });
  });

  document.addEventListener('click', (e) => {
    if (!selector.contains(e.target)) {
      selector.classList.remove('open');
      btn.setAttribute('aria-expanded', 'false');
    }
  });

  const saved = localStorage.getItem('skardu_lang');
  if (saved && TRANSLATIONS[saved]) { applyLanguage(saved); }
}

document.addEventListener('DOMContentLoaded', initLanguageSelector);
